// AND operator
// condition will be true if both are true
let personTrusted = true;
let personGrades = true;
console.log("AND operator");
let egligiblePerson = personTrusted && personGrades;
console.log(egligiblePerson);

// OR operator
// condition will be true if one of the conditions is true
console.log("==================");
console.log("OR operator");
let egligibleLoanTaker = personTrusted || personGrades;
console.log(egligibleLoanTaker);
