function getMinimumCost(k, c) {
  let length = c.length;
  c.sort((a, b) => b - a);
  let round = 0;
  let total = 0;

  let i = 0;
  while (length !== 0) {
    let cost = (round + 1) * c[i];
    if ((i + 1) % k === 0 && length > 0) round++;
    length--;
    i++;
    total += cost;
  }
  return total;
}

