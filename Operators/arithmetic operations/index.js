let x = 5;
let y = 2;

// assignment operators
console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
// x's power y
console.log(x ** y);

// increment operator
console.log(++x);
console.log(y++);
console.log(y);
// deccrement operator
console.log(--x);
console.log(y--);
console.log(y);
